    public void preencherTabelaNotas(ArrayList<Notas> notas) {
        DefaultTableModel Tabela = (DefaultTableModel) TabelaNotas.Tabela_Notas.getModel();
        
        // Limpar a tabela antes de inserir novas notas
        Tabela.setRowCount(0);
        
        // Preencher a tabela com as notas
        for (Notas obj : notas) {
            Tabela.addRow(new Object[]{
                obj.nome, obj.codigo, obj.nomeTurma,
                obj.getNota1(), obj.getNota2(), obj.getNota3(),
                obj.getNota4(), obj.getNota5(), obj.getNota6(),
                obj.getNota7(), obj.getNota8(), obj.getNota9(),
                obj.getNota10()
            });
        }
    }

